    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            elCliente.getCodigo();//Fuerzo la excepcion
            String cod = String.valueOf(model.getValueAt(jTableClientes.getSelectedRow(),0));
            int reply = JOptionPane.showConfirmDialog(null, "¿Esta seguro que desea eliminar este cliente? Esta acción no se puede deshacer","Confirmacion",JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION){
                Iterator i;
                i = setPedidos.iterator();
                while(i.hasNext()){
                    Pedido ped = (Pedido) i.next();
                    if(ped.getCodigoCliente() == elCliente.getCodigo())
                        i.remove();
                }
                setClientes.remove(elCliente);
                JOptionPane.showMessageDialog(null, "Cliente eliminado","Información",JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
                padre.setVisible(true);
                this.setVisible(true);
                elCliente = null;
            }
        }
        catch(NullPointerException npe){
            JOptionPane.showMessageDialog(null, "Debe seleccionar un cliente","Advertencia",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTableClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClientesMouseClicked
        Iterator i;
        String cod = String.valueOf(model.getValueAt(jTableClientes.getSelectedRow(),0));
        int codigo = Integer.parseInt(cod);
        i = setClientes.iterator();
        while(i.hasNext()){
            Cliente clienteI = (Cliente) i.next();
            if(codigo == clienteI.getCodigo()){
                elCliente = clienteI;
            }
        }
    }//GEN-LAST:event_jTableClientesMouseClicked

