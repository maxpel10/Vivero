    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        Iterator i;
        model.setRowCount(0);
        String buscado = jTextField1.getText();
        buscado = buscado.toUpperCase();
        i = setClientes.iterator();
        while(i.hasNext()){
            Cliente clienteI = (Cliente) i.next();
            String clienteado = clienteI.getNombre();
            clienteado = clienteado.toUpperCase();
            if(clienteado.contains(buscado)){
                String tipo;
                if(clienteI.getTipo())
                    tipo = "Vivero";
                else
                    tipo = "Floreria";
                model.addRow(new Object[]{String.valueOf(clienteI.getCodigo()),clienteI.getNombre(),clienteI.getDireccion(),clienteI.getEmail(),String.valueOf(clienteI.getCuil()),tipo,clienteI.getUsuario(),clienteI.getFechaDeIngreso()});
            }
        }
    }//GEN-LAST:event_jTextField1KeyTyped

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        model.setRowCount(0);
        if(setClientes.isEmpty()){
            JOptionPane.showMessageDialog(null, "No hay clientes cargados en el sistema.","Información",JOptionPane.INFORMATION_MESSAGE);
            padre.setEnabled(true);
            this.dispose();
        }
        Iterator i;
        i = setClientes.iterator();
        while(i.hasNext()){
            Cliente clienteI = (Cliente) i.next();
            String tipo;
            if(clienteI.getTipo())
                tipo = "Vivero";
            else
                tipo = "Floreria";
            model.addRow(new Object[]{String.valueOf(clienteI.getCodigo()),clienteI.getNombre(),clienteI.getDireccion(),clienteI.getEmail(),String.valueOf(clienteI.getCuil()),tipo,clienteI.getUsuario(),clienteI.getFechaDeIngreso()});
        }
        elCliente = null;
    }//GEN-LAST:event_formComponentShown

