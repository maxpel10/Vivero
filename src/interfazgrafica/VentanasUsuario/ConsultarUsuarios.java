    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        jRadioButtonOrdenarPorDNI.setSelected(true);
        listaDuenosOrdenada = new ArrayList();
        listaEmpleadosOrdenada = new ArrayList();
        Iterator i = setUsuarios.iterator();
        while(i.hasNext()){
            Usuario u = (Usuario) i.next();
            if(u instanceof Dueño){
                Dueño d = (Dueño) u;
                listaDuenosOrdenada.add(d); 
            }
            else{
                Empleado e = (Empleado) u;
                listaEmpleadosOrdenada.add(e);
            }
        }
        modelDuenos.setRowCount(0);
        modelEmpleados.setRowCount(0);
        Collections.sort(listaDuenosOrdenada, new OrdenarDuenoPorDni());
        Collections.sort(listaEmpleadosOrdenada, new OrdenarEmpleadoPorDni());
        Iterator k = listaDuenosOrdenada.listIterator();
        while(k.hasNext()){
            Dueño d = (Dueño) k.next();
            modelDuenos.addRow(new Object []{d.getNombre(),d.getApellido(),d.getDni(),d.getTelefonoMovil(),d.getTelefonoFijo(),d.getFechaDeNacimiento(),d.getPuntaje(),d.getEspeciesCreadas(),d.getEscala()});
        }
        Iterator j = listaEmpleadosOrdenada.listIterator();
        while(j.hasNext()){
            Empleado e = (Empleado) j.next();
            modelEmpleados.addRow(new Object []{e.getNombre(),e.getApellido(),e.getDni(),e.getTelefonoMovil(),e.getTelefonoFijo(),e.getFechaDeNacimiento(),e.getPuntaje(),e.getSueldo(),e.getAntiguedad(),e.getBonoExtra()});
        }
    }//GEN-LAST:event_formComponentShown

