    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int reply = JOptionPane.showConfirmDialog(null,"Esta seguro que desea evaluar usuarios? Esta accion no se puede deshacer","Atencion",JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION){
            Iterator i = setUsuarios.iterator();
            while(i.hasNext()){
                Usuario u = (Usuario) i.next();
                u.evaluacionDeUsuario();
            }
            JOptionPane.showMessageDialog(null,"La evaluacion de usuarios se realizo correctamente.","Info",JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
            padre.setVisible(true);
            this.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

