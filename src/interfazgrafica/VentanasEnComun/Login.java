    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String user = jTextFieldUserLogin.getText();
        String contra = new String(jPasswordFieldContraLogin.getPassword());
        boolean estaBien = true;
        boolean found = false;
        Usuario usuario = null;
        Cliente cliente = null;
        
        if (user.equals("") || contra.equals("")){
            JOptionPane.showMessageDialog(null,"Algunos campos no se han completado. Intente nuevamente." ,"Atencion" , JOptionPane.WARNING_MESSAGE);
            estaBien = false;
        }
        
        if (estaBien){
            Iterator i = setClientes.iterator();
            while(i.hasNext()){
                cliente =(Cliente)i.next();
                if (cliente.getUsuario().equals(user) && cliente.getContrasenia().equals(contra)){
                    found = true;
                    break;
                }
            }
            if(found){
                MenuClientes mc = new MenuClientes(setClientes,cliente,setUsuarios);
                mc.setVisible(true);
                mc.setLocationRelativeTo(this);
                this.dispose();
            }
            else{
                Iterator j = setUsuarios.iterator();
                while(j.hasNext()){
                    usuario =(Usuario)j.next();
                    if (usuario.getUsuario().equals(user) && usuario.getContrasenia().equals(contra)){
                        found = true;
                        break;
                    }
                }
                if(found){
                    MenuPrincipal mp = new MenuPrincipal(setUsuarios,setClientes,usuario);
                    mp.setLocationRelativeTo(null);
                    mp.setVisible(true);
                    this.dispose();
                }
                else
                    JOptionPane.showMessageDialog(null,"El usuario o la contraseña son inválidos. Intente nuevamente." ,"Atencion" , JOptionPane.WARNING_MESSAGE);
            }
        }
                
    }//GEN-LAST:event_jButtonLoginActionPerformed

