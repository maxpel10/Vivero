    public void inUsuarios() throws FileNotFoundException, IOException{
       File f = new File("Usuarios.txt");
       FileReader leo = new FileReader(f);
       BufferedReader leer = new BufferedReader(leo);
       String tipo,auxParse;
       while((tipo = leer.readLine()) != null){
            if(tipo.equals("Dueno")){
                Dueño aux1 = new Dueño();
                aux1.setNombre(leer.readLine().substring(8));
                aux1.setApellido(leer.readLine().substring(10));
                auxParse = leer.readLine().substring(5);
                aux1.setDni(Integer.parseInt(auxParse));
                aux1.setTelefonoMovil(Long.parseLong(leer.readLine().substring(16)));
                aux1.setTelefonoFijo(Long.parseLong(leer.readLine().substring(15)));
                aux1.setFechaDeNacimiento(leer.readLine().substring(21));
                aux1.setUsuario(leer.readLine().substring(9));
                aux1.setContrasenia(leer.readLine().substring(12));
                auxParse = leer.readLine().substring(9);
                aux1.setPuntaje(Integer.parseInt(auxParse));
                auxParse = leer.readLine().substring(18);
                aux1.setEspeciesCreadas(Integer.parseInt(auxParse));
                auxParse = leer.readLine().substring(8);
                aux1.setEscala(Integer.parseInt(auxParse));
                setUsuarios.add(aux1);
                leer.readLine();
            }
            else
            {
                Empleado aux2 = new Empleado();
                aux2.setNombre(leer.readLine().substring(8));
                aux2.setApellido(leer.readLine().substring(10));
                auxParse = leer.readLine().substring(5);
                aux2.setDni(Integer.parseInt(auxParse));
                aux2.setTelefonoMovil(Long.parseLong(leer.readLine().substring(16)));
                aux2.setTelefonoFijo(Long.parseLong(leer.readLine().substring(15)));
                aux2.setFechaDeNacimiento(leer.readLine().substring(21));
                aux2.setUsuario(leer.readLine().substring(9));
                aux2.setContrasenia(leer.readLine().substring(12));
                auxParse = leer.readLine().substring(9);
                aux2.setPuntaje(Integer.parseInt(auxParse));
                auxParse = leer.readLine().substring(8);
                aux2.setSueldo(Double.parseDouble(auxParse));
                auxParse = leer.readLine().substring(12);
                aux2.setAntiguedad(Integer.parseInt(auxParse));
                auxParse = leer.readLine().substring(12);
                aux2.setBonoExtra(Double.parseDouble(auxParse));
                setUsuarios.add(aux2);
                leer.readLine();
               
            }
        }
        leer.close();
       }
    
    public void inClientes() throws FileNotFoundException, IOException{
       File f = new File("Clientes.txt");
       FileReader leo = new FileReader(f);
       BufferedReader leer = new BufferedReader(leo);
       String auxParse;
       while((auxParse = leer.readLine()) != null){
            Cliente cliente = new Cliente();
            cliente.setCodigo(Integer.parseInt(auxParse.substring(20)));
            cliente.setNombre(leer.readLine().substring(8));
            cliente.setDireccion(leer.readLine().substring(11));
            cliente.setEmail(leer.readLine().substring(8));
            auxParse = leer.readLine();
            cliente.setCuil(Long.parseLong(auxParse.substring(6)));
            auxParse = leer.readLine();
            cliente.setTipo(Boolean.parseBoolean(auxParse.substring(6)));
            cliente.setFechaDeIngreso(leer.readLine().substring(18));
            cliente.setUsuario(leer.readLine().substring(9));
            cliente.setContrasenia(leer.readLine().substring(12));
            setClientes.add(cliente);
            leer.readLine();
       }
       leer.close();
    }
    
    public void outClientes() throws IOException{
        File f = new File("Clientes.txt");
        FileWriter escribo = new FileWriter(f);
        BufferedWriter escribir = new BufferedWriter(escribo);
        Iterator i = this.setClientes.iterator();
        while(i.hasNext()){
            Cliente c = (Cliente)i.next();
            escribir.write(c.toString());
            escribir.newLine();
        }
        escribir.close();
    }
    
